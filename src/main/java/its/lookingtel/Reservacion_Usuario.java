    private void setFecha_Partida() {

        if (jDateChooser1.getDate() != null) {

            Calendar calendar = Calendar.getInstance();
            calendar.setTime(jDateChooser1.getDate());
            calendar.add(Calendar.DAY_OF_MONTH, (int) jSpinner2.getValue());
            Date finaldate = calendar.getTime();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            String convertedDate = dateFormat.format(finaldate);

            try {
                Date formattedDate = dateFormat.parse(convertedDate);
                jDateChooser2.setDate(formattedDate);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }
