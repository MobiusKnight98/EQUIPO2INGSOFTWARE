    private void setServices() {

        List<String> services = Arrays.asList(this.condominio.servicios.split(","));
        System.out.println(services);
        if (services.contains("Agua")) {
            jCheckBox1.setSelected(true);
        }
        if (services.contains("Luz")) {
            jCheckBox3.setSelected(true);
        }
        if (services.contains("Gas")) {
            jCheckBox2.setSelected(true);
        }
        if (services.contains("Cocina")) {
            jCheckBox4.setSelected(true);
        }
        if (services.contains("Internet")) {
            jCheckBox5.setSelected(true);
        }
        if (services.contains("Televisi贸n")) {
            jCheckBox6.setSelected(true);
        }

    }

    private void setImage() {

        try {

            // Load the image from the URL
            URL imageUrl = new URL(this.condominio.image_lugar);
            Image image = ImageIO.read(imageUrl);
            Image scaledImage = image.getScaledInstance(jPanel13.getPreferredSize().width,
                    jPanel13.getPreferredSize().height,
                    Image.SCALE_SMOOTH);

            // Create an ImageIcon from the image
            ImageIcon icon = new ImageIcon(scaledImage);

            // Set the icon on the JLabel
            jLabel24.setIcon(icon);

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "No se pueden mostrar las imagenes", "Error", 0);
        }

    }

